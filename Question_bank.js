(function validateQB(){
  const bank = window.QUESTION_BANK;
  const issues = [];

  if(!bank || typeof bank!=="object") {
    issues.push("QUESTION_BANK root is not an object");
  }

  for (const [subject, obj] of Object.entries(bank || {})) {
    if(!obj || typeof obj!=="object"){
      issues.push(`"${subject}" is not an object`);
      continue;
    }

    for (const lvl of ["easy","medium","hard"]) {
      const arr = obj[lvl];

      if(!Array.isArray(arr)){
        issues.push(`"${subject}.${lvl}" is missing or not an array`);
        continue;
      }

      // Duplicate control (per subject+level)
      const seen = new Set();

      arr.forEach((q,i)=>{
        if(!q || typeof q!=="object"){
          issues.push(`"${subject}.${lvl}[${i}]" not an object`);
          return;
        }

        if(q.type!=="mcq") issues.push(`"${subject}.${lvl}[${i}]" type must be "mcq"`);
        if(typeof q.question!=="string" || !q.question.trim()) issues.push(`"${subject}.${lvl}[${i}]" missing question`);
        if(!Array.isArray(q.options) || q.options.length < 2) issues.push(`"${subject}.${lvl}[${i}]" bad options`);
        if(typeof q.answer!=="number" || q.answer < 0 || q.answer >= (q.options ? q.options.length : 0)){
          issues.push(`"${subject}.${lvl}[${i}]" answer out of range`);
        }

        // Duplicate by question text
        const key = (q.question || "").trim();
        if(key){
          if(seen.has(key)){
            issues.push(`Duplicate question: "${subject}.${lvl}[${i}]" -> ${key}`);
          } else {
            seen.add(key);
          }
        }
      });
    }
  }

  if(issues.length){
    console.error("QUESTION_BANK validation failed:", issues);
    throw new Error("QUESTION_BANK validation failed. Check console for details.");
  }
})();
